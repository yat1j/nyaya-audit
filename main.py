# ── Internal helpers ──────────────────────────────────────────────────────────

def _cosine_sim(u: np.ndarray, v: np.ndarray) -> float:
    return float(1.0 - scipy_cosine(u, v))


def _normalise(scores: np.ndarray) -> np.ndarray:
    """Min-max normalise scores to [0, 1]."""
    mn, mx = scores.min(), scores.max()
    if mx == mn:
        return np.ones_like(scores) * 0.5
    return (scores - mn) / (mx - mn)


def _read_csv_from_bytes(contents: bytes) -> pd.DataFrame:
    """Write bytes to a temp file, read as DataFrame, delete temp file."""
    with tempfile.NamedTemporaryFile(delete=False, suffix=".csv", mode="wb") as t:
        t.write(contents)
        path = t.name
    df = pd.read_csv(path)
    os.unlink(path)
    return df


def _demographic_parity(df: pd.DataFrame, score_col: str) -> dict:
    """
    Compute demographic parity ratio between brahmin and dalit groups.
    Requires a 'true_group' column in the DataFrame.
    Returns parity ratio (0-1) and whether it passes the 80/20 rule.
    """
    if "true_group" not in df.columns:
        return {
            "parity": None,
            "passes_fairness": None,
            "group_shortlist_rates": {},
        }

    scores    = df[score_col].values
    threshold = np.percentile(scores, 50)  # top 50% = shortlisted
    shortlisted = (scores >= threshold).astype(int)

    rates = {}
    for group in ["brahmin", "dalit"]:
        mask = (df["true_group"] == group).values
        if mask.sum() > 0:
            rates[group] = float(shortlisted[mask].mean())

    if len(rates) < 2:
        return {
            "parity": None,
            "passes_fairness": None,
            "group_shortlist_rates": {g: round(r * 100, 1) for g, r in rates.items()},
        }

    # Parity = min_rate / max_rate. Passes if >= 0.80 (80/20 rule)
    parity = min(rates.values()) / max(rates.values())
    return {
        "parity":             round(parity, 4),
        "passes_fairness":    bool(parity >= 0.80),
        "group_shortlist_rates": {g: round(r * 100, 1) for g, r in rates.items()},
    }


